--
-- DESIGN DECISIONS DOCUMENTED HERE:
--   - OLTP (not OLAP): dataset is ~5,630 rows. PostgreSQL handles
--     analytics queries at this scale in <10ms. OLAP is for 10M+ rows.
--   - Two customer tables: 'customers' (static profile, never changes)
--     and 'customer_features' (behavioral snapshot, updated monthly).
--     Reason: separates who the customer IS from what they DO.
--   - ENUMs for all categorical columns: enforces valid values at the
--     DB layer — no invalid string can enter the database ever.
--   - JSONB for flexible fields (SHAP reasons, feature snapshots):
--     these structures vary per prediction and are queried by content.
