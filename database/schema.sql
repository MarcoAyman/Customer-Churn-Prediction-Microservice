-- ============================================================
-- SECTION 1 — ENUMS
--
-- WHY ENUMS?
--   An ENUM is a data type with a fixed list of allowed values.
--   If you try to insert 'CC' into a payment_mode_enum column,
--   PostgreSQL rejects it with an error — it only accepts 'Credit Card'.
--   This means the database enforces data quality automatically.
--   No invalid value can ever enter the database, even if a bug
--   in the application layer tries to insert one.
--
-- IMPORTANT: ENUMs must be created BEFORE the tables that use them.
-- ============================================================

-- Payment methods — normalised from Kaggle dirty values in Stage 1
-- 'CC' and 'Cash on Delivery' were cleaned to canonical values before insert
CREATE TYPE payment_mode_enum AS ENUM (
    'COD',          -- Cash on Delivery
    'Credit Card',  -- also covers former alias 'CC'
    'Debit Card',
    'E wallet',
    'UPI'
);

-- Login device types
-- 'Phone' was normalised to 'Mobile Phone' in Stage 1 cleaning
CREATE TYPE login_device_enum AS ENUM (
    'Mobile Phone', -- covers former alias 'Phone'
    'Computer'
);

-- Product categories the customer prefers to order
-- 'Mobile Phone' (as category) was normalised to 'Mobile' in Stage 1
CREATE TYPE order_cat_enum AS ENUM (
    'Grocery',
    'Fashion',
    'Mobile',           -- covers former alias 'Mobile Phone'
    'Laptop & Accessory',
    'Others'
);

-- Customer gender
CREATE TYPE gender_enum AS ENUM (
    'Male',
    'Female'
);

-- Customer marital status
CREATE TYPE marital_status_enum AS ENUM (
    'Single',
    'Married',
    'Divorced'
);

-- ML model lifecycle status
-- A model moves through these stages: candidate → staging → production
-- Only ONE model should have status='production' at any time
CREATE TYPE model_status_enum AS ENUM (
    'candidate',    -- freshly trained, not yet evaluated
    'staging',      -- passed evaluation gate, being shadow-tested
    'production',   -- currently serving all predictions
    'archived'      -- replaced or retired — kept for audit history
);

-- Batch scoring job status
CREATE TYPE batch_status_enum AS ENUM (
    'running',      -- currently executing
    'completed',    -- finished successfully
    'failed',       -- crashed with error (see error_message column)
    'aborted'       -- manually stopped
);

-- Risk tier assigned to each customer after scoring
CREATE TYPE risk_tier_enum AS ENUM (
    'HIGH',         -- churn_probability >= 0.70
    'MEDIUM',       -- churn_probability 0.45 – 0.69
    'LOW',          -- churn_probability < 0.45
    'ONBOARDING'    -- customer not yet scoreable (tenure < 1 month or no orders)
);

-- Event types for the SSE (Server-Sent Events) admin dashboard feed
CREATE TYPE sse_event_type_enum AS ENUM (
    'new_customer',      -- a new customer registered
    'high_churn_alert',  -- a customer crossed HIGH risk threshold
    'batch_completed',   -- batch scoring run finished
    'drift_alert',       -- feature drift detected (PSI > 0.20)
    'model_promoted',    -- a new model version went to production
    'ping'               -- keepalive event — prevents Render from closing idle SSE connections
);

-- Where feature data came from (for auditability)
CREATE TYPE features_source_enum AS ENUM (
    'kaggle_seed',  -- inserted from the Kaggle CSV via seed_database.py
    'system',       -- computed by the FastAPI background job from real user actions
    'synthetic'     -- generated by generate_customers.py for demo/testing
);


-- ============================================================
-- SECTION 2 — TABLE 1: customers
--
-- PURPOSE:
--   Stores who the customer IS. Static profile data only.
--   This data is set at registration and almost never changes.
--   Behavioral data (what they DO each month) lives in customer_features.
--
-- WHY SPLIT FROM customer_features?
--   Gender, CityTier, PreferredPaymentMode — these never change.
--   OrderCount, DaySinceLastOrder, Tenure — these change every month.
--   Mixing them means every monthly background job UPDATE touches the
--   entire row including the static columns that didn't change.
--   Splitting them means updates only touch the rows and tables that
--   actually need updating. Cleaner, faster, and easier to reason about.
--
-- SOURCE OF DATA:
--   Kaggle rows:  seeded from data/cleaned/ecommerce_churn_clean.csv
--   Live rows:    inserted by POST /api/v1/customers/register endpoint
-- ============================================================

CREATE TABLE customers (

    -- ── Identity ────────────────────────────────────────────
    -- UUID is generated by PostgreSQL automatically at INSERT.
    -- The user never provides this. The frontend never sends this.
    -- gen_random_uuid() is a PostgreSQL built-in function.
    id                      UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Original CustomerID from the Kaggle CSV.
    -- Only populated for seeded rows — NULL for all live registrations.
    -- Stored so we can trace a row back to the original dataset if needed.
    kaggle_customer_id      INTEGER         UNIQUE,

    -- Email and password are NULL for Kaggle-seeded rows
    -- (the Kaggle dataset has no email addresses or passwords).
    -- Required for live registrations.
    email                   VARCHAR(255)    UNIQUE,
    password_hash           VARCHAR(255),   -- bcrypt hash, min cost=12. NEVER store plain text.

    -- Display name — NULL for Kaggle rows (dataset has no names)
    full_name               VARCHAR(255),

    -- Role controls what the user can access in the frontend.
    -- 'customer' sees their own profile only.
    -- 'admin'    sees the full dashboard, batch results, monitoring.
    role                    VARCHAR(20)     NOT NULL DEFAULT 'customer'
                            CHECK (role IN ('customer', 'admin')),


    -- ── Static Profile Features ─────────────────────────────
    -- These are the Kaggle columns that belong to the customer permanently.
    -- All use ENUMs defined above — invalid values are rejected by PostgreSQL.
    gender                  gender_enum,
    marital_status          marital_status_enum,

    -- CityTier: 1=metro, 2=mid-size, 3=smaller city
    -- Stored as SMALLINT (2 bytes) — only needs values 1, 2, 3
    city_tier               SMALLINT        CHECK (city_tier IN (1, 2, 3)),

    preferred_payment_mode  payment_mode_enum,
    preferred_login_device  login_device_enum,
    preferred_order_cat     order_cat_enum,


    -- ── Kaggle Ground Truth ─────────────────────────────────
    -- The churn label from the Kaggle dataset (0=retained, 1=churned).
    -- This is stored HERE for reference only — it is historical data.
    -- It is NOT used as input to the live model.
    -- At seeding time, this value is also copied into the predictions table
    -- as ground_truth so the model can be evaluated against real outcomes.
    kaggle_churn_label      SMALLINT        CHECK (kaggle_churn_label IN (0, 1)),


    -- ── Lifecycle ────────────────────────────────────────────
    -- Soft delete: we never DELETE customers from the database.
    -- Setting is_active = FALSE excludes them from batch scoring and dashboards.
    is_active               BOOLEAN         NOT NULL DEFAULT TRUE,

    -- registered_at is the anchor for computing Tenure.
    -- Tenure = (TODAY - registered_at) in months, recomputed daily.
    registered_at           TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    -- updated_at is maintained by a trigger (defined in Section 6).
    -- It updates automatically whenever any column in this row changes.
    updated_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);

-- Index on city_tier: dashboard filter "show customers in City Tier 3"
CREATE INDEX idx_customers_city_tier     ON customers(city_tier);

-- Index on role: authentication check "is this user an admin?"
CREATE INDEX idx_customers_role          ON customers(role);

-- Index on is_active: batch scoring query filters WHERE is_active = TRUE
CREATE INDEX idx_customers_is_active     ON customers(is_active);

-- Index on registered_at descending: "show newest customers first" query
CREATE INDEX idx_customers_registered_at ON customers(registered_at DESC);

-- Table and column comments — visible in Supabase dashboard
COMMENT ON TABLE customers IS
    'Static customer profile. Who the customer IS. '
    'Behavioral snapshot data (what they DO) lives in customer_features. '
    'kaggle_churn_label is Kaggle training reference only — not a live prediction input.';

COMMENT ON COLUMN customers.id IS
    'UUID generated by PostgreSQL at INSERT. Never provided by the user or frontend.';

COMMENT ON COLUMN customers.kaggle_customer_id IS
    'Original CustomerID from Kaggle CSV. NULL for all live-registered customers.';

COMMENT ON COLUMN customers.kaggle_churn_label IS
    'Churn label from Kaggle dataset (0=retained, 1=churned). '
    'Copied to predictions.ground_truth at seeding time. '
    'Not used as model input — historical reference only.';


-- ============================================================
-- SECTION 3 — TABLE 2: customer_features
--
-- PURPOSE:
--   Stores what the customer DOES — behavioral snapshot data.
--   One row per customer. Always the LATEST snapshot.
--   Updated by background jobs after user actions and on the daily cron.
--
-- WHY ONE ROW PER CUSTOMER (not a history table)?
--   The ML model scores the customer's CURRENT state.
--   It does not need last month's OrderCount — it needs today's.
--   One row per customer keeps the batch scoring query simple:
--     SELECT * FROM customer_features WHERE is in eligible set → score all.
--   Feature history for drift analysis is tracked in drift_reports (Table 6).
--
-- UPDATE SCHEDULE:
--   Tenure         → daily cron job (recomputed from registered_at)
--   OrderCount     → immediately after each order is placed
--   DaySinceLastOrder → immediately after each order is placed
--   Complain       → immediately when support ticket is raised
--   Everything else → every 20-day batch cycle refresh
-- ============================================================

CREATE TABLE customer_features (

    id                              UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Foreign key to customers table.
    -- ON DELETE CASCADE: if a customer is deleted, their features are also deleted.
    -- UNIQUE ensures one row per customer — enforced at DB level.
    customer_id                     UUID            NOT NULL
                                    REFERENCES customers(id) ON DELETE CASCADE,
    UNIQUE (customer_id),  -- one feature snapshot per customer at all times


    -- ── Service Relationship Features ───────────────────────
    -- How long the customer has been on the platform (months).
    -- Recomputed daily: tenure_months = (TODAY - customers.registered_at) / 30
    -- NUMERIC(5,1): up to 9999.9 months — enough for any realistic tenure
    tenure_months                   NUMERIC(5,1)    CHECK (tenure_months >= 0),

    -- Self-reported satisfaction on a 1–5 scale.
    -- Combined with Complain in feature engineering to detect silent dissatisfaction.
    satisfaction_score              SMALLINT        CHECK (satisfaction_score BETWEEN 1 AND 5),

    -- Whether the customer filed a complaint in the last month.
    -- Strongest single-column churn predictor in this dataset.
    -- Updated immediately when support ticket is raised.
    complain                        BOOLEAN         NOT NULL DEFAULT FALSE,

    -- Distance in km from the fulfillment warehouse to customer address.
    -- Longer distance = slower delivery = higher dissatisfaction risk.
    warehouse_to_home               NUMERIC(6,1)    CHECK (warehouse_to_home > 0),

    -- Number of saved delivery addresses on the account.
    -- More addresses = deeper platform embeddedness = harder to switch.
    number_of_address               SMALLINT        CHECK (number_of_address >= 1),


    -- ── Engagement & Behavior Features ──────────────────────
    -- Average hours per month spent on the app.
    -- Clearest engagement leading indicator — low time precedes churn by weeks.
    hour_spend_on_app               NUMERIC(4,1)    CHECK (hour_spend_on_app >= 0
                                                     AND hour_spend_on_app <= 24),

    -- How many devices the customer has registered to their account.
    -- More devices = higher ecosystem lock-in = lower churn risk.
    number_of_device_registered     SMALLINT        CHECK (number_of_device_registered >= 1),

    -- Days since the customer's last order.
    -- The recency 'R' in RFM. Long gap + low tenure = very high churn risk.
    -- Updated immediately after each order is placed.
    day_since_last_order            SMALLINT        CHECK (day_since_last_order >= 0),


    -- ── Transaction History Features ────────────────────────
    -- Number of orders placed in the last month.
    -- Core frequency metric. Sudden drop from baseline = early churn signal.
    order_count                     SMALLINT        CHECK (order_count >= 0),

    -- Year-over-year percentage change in order value.
    -- IMPORTANT: This column CAN be negative (customer spent less than last year).
    -- Negative values = slow-burn churn signal. DO NOT clip or set lower bound.
    -- No CHECK constraint — negatives are valid and important.
    order_amount_hike_from_last_year NUMERIC(6,2),

    -- Number of coupons used last month.
    -- High value = discount-dependent customer. Loyalty evaporates when offers stop.
    coupon_used                     SMALLINT        CHECK (coupon_used >= 0),

    -- Total cashback earned last month.
    -- Correlated with order activity but incentive-driven — can mask disengagement.
    cashback_amount                 NUMERIC(10,2)   CHECK (cashback_amount >= 0),


    -- ── Metadata ─────────────────────────────────────────────
    -- When these feature values were last computed/updated.
    -- Used to detect stale features: if features_computed_at is > 25 days ago,
    -- the batch scoring job should refresh before scoring.
    features_computed_at            TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    -- Where this feature data came from — for auditability.
    features_source                 features_source_enum NOT NULL DEFAULT 'system'
);

-- Index for the core batch scoring query:
--   SELECT * FROM customer_features WHERE customer_id IN (eligible_ids)
CREATE INDEX idx_cf_customer_id      ON customer_features(customer_id);

-- Index for the scoring eligibility gate query:
--   WHERE tenure_months >= 1 AND order_count >= 1
CREATE INDEX idx_cf_tenure           ON customer_features(tenure_months);
CREATE INDEX idx_cf_order_count      ON customer_features(order_count);

-- Index for drift monitoring: order by recency
CREATE INDEX idx_cf_computed_at      ON customer_features(features_computed_at DESC);

COMMENT ON TABLE customer_features IS
    'Behavioral snapshot per customer. One row = latest state. '
    'Updated by background jobs. Read by ML batch scoring via v_customer_ml_features view. '
    'Feature engineering (OHE, scaling, derived signals) happens in Python — NOT stored here.';

COMMENT ON COLUMN customer_features.order_amount_hike_from_last_year IS
    'YoY % change in order value. Negatives are valid (declining spend = churn signal). '
    'No lower bound constraint — do not clip this value anywhere in the pipeline.';


-- ============================================================
-- SECTION 4 — TABLE 3: model_versions
--
-- PURPOSE:
--   Operational model registry. The API reads this table on startup
--   to know which model version to download from HuggingFace Hub.
--
-- RELATIONSHIP TO MLFLOW:
--   MLflow tracks experiments on your LOCAL machine during training.
--   MLflow does not run in production (it is not deployed to Render).
--   This table IS the production model registry.
--   After training, you run register.py which:
--     1. Pushes model.pkl to HuggingFace Hub
--     2. Inserts a row into THIS table
--     3. Updates the status of the previous production model to 'archived'
--
-- ONLY ONE ROW should have status = 'production' at any time.
--   This is enforced by application logic in register.py — not a DB constraint
--   because we need to update old→archived and new→production atomically.
-- ============================================================

CREATE TABLE model_versions (

    id                  UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Semantic version string: 'v1.0.0', 'v1.1.0', 'v2.0.0'
    -- UNIQUE: two models cannot share a version number
    version             VARCHAR(20)     NOT NULL UNIQUE,

    -- Lifecycle status — see model_status_enum above
    status              model_status_enum NOT NULL DEFAULT 'candidate',


    -- ── Performance Metrics ──────────────────────────────────
    -- Copied from MLflow at registration time.
    -- Stored here so the dashboard can show "current model: AUC=0.87" without
    -- needing to call MLflow (which is local and not accessible from Render).
    auc_roc             NUMERIC(6,4)    CHECK (auc_roc BETWEEN 0 AND 1),
    pr_auc              NUMERIC(6,4)    CHECK (pr_auc BETWEEN 0 AND 1),
    recall_score        NUMERIC(6,4)    CHECK (recall_score BETWEEN 0 AND 1),
    precision_score     NUMERIC(6,4)    CHECK (precision_score BETWEEN 0 AND 1),
    f1_score            NUMERIC(6,4)    CHECK (f1_score BETWEEN 0 AND 1),
    brier_score         NUMERIC(6,4),   -- calibration quality — lower is better
    -- Custom business cost metric: (FN × $150) + (FP × $15)
    -- A False Negative (missing a churner) costs $150. A False Positive costs $15.
    business_cost_score NUMERIC(10,2),


    -- ── Configuration ────────────────────────────────────────
    -- The probability threshold used to convert churn_probability → churn_label.
    -- NEVER defaults to 0.5 — tuned from the PR curve to maximise recall.
    -- This threshold is stored per model version so predictions are reproducible.
    decision_threshold  NUMERIC(4,3)    NOT NULL DEFAULT 0.65
                        CHECK (decision_threshold BETWEEN 0 AND 1),

    -- Which algorithm produced this model
    algorithm           VARCHAR(50),    -- e.g. 'XGBoostClassifier'

    -- MD5 hash of the feature_names.json file used during training.
    -- If this hash changes between versions, the feature schema changed —
    -- a critical warning that old and new models are not directly comparable.
    feature_set_hash    VARCHAR(64),


    -- ── Training Context ─────────────────────────────────────
    train_rows          INTEGER,        -- how many rows were in the training split
    val_rows            INTEGER,        -- validation split size
    test_rows           INTEGER,        -- test split size (held out, never touched during tuning)
    train_date          DATE,           -- when training was run

    -- Link back to the MLflow experiment run for full reproducibility.
    -- You can use this ID to re-examine the run in your local MLflow UI.
    mlflow_run_id       VARCHAR(255),


    -- ── Artifact Location ────────────────────────────────────
    -- Path on HuggingFace Hub where model.pkl, preprocessor.pkl,
    -- and feature_names.json are stored for this version.
    -- The API uses this path to download artifacts on startup.
    artifact_path       VARCHAR(500),   -- e.g. 'your-hf-username/churnguard/v1.0.0/'


    -- ── Promotion Tracking ───────────────────────────────────
    promoted_at         TIMESTAMPTZ,    -- when this model went to production
    promoted_by         VARCHAR(100),   -- 'auto' (CI/CD) or your GitHub username
    promotion_notes     TEXT,           -- e.g. 'Improved recall from 0.78 to 0.83'


    -- ── Lifecycle ────────────────────────────────────────────
    created_at          TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    archived_at         TIMESTAMPTZ     -- set when status changes to 'archived'
);

-- Index for API startup query: "which model is currently in production?"
CREATE INDEX idx_mv_status      ON model_versions(status);

-- Index for dashboard: "show model history newest first"
CREATE INDEX idx_mv_created_at  ON model_versions(created_at DESC);

COMMENT ON TABLE model_versions IS
    'Operational model registry. One row per trained model version. '
    'API reads this on startup to download the production model from HuggingFace Hub. '
    'MLflow is for local experiment tracking only — this table is the production source of truth. '
    'Only one row should have status=production at any time.';


-- ============================================================
-- SECTION 5 — TABLE 4: predictions
--
-- PURPOSE:
--   Complete audit trail of every prediction ever made.
--   Both real-time (single customer API call) and batch (20-day cycle).
--
-- WHY STORE features_snapshot AS JSONB?
--   When a customer churns 30 days after their last prediction,
--   you want to ask: "what did their features look like when we scored them?"
--   Without the snapshot, you only have the probability — no way to debug
--   why the model gave that score or to detect data drift retrospectively.
--   JSONB stores the exact feature values used — making every prediction
--   fully auditable and debuggable.
--
-- WHY STORE ground_truth SEPARATELY FROM kaggle_churn_label?
--   kaggle_churn_label (in customers table) is historical training data.
--   ground_truth here is the OBSERVED real outcome after prediction.
--   For Kaggle rows: copied from kaggle_churn_label at seeding time.
--   For live rows: filled in async after 30+ days when outcome is known.
--   These are conceptually different — one is training data, one is
--   production feedback for ongoing model evaluation.
-- ============================================================

CREATE TABLE predictions (

    id                  UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Which customer was scored
    customer_id         UUID            NOT NULL REFERENCES customers(id),

    -- Which batch run produced this prediction.
    -- NULL for real-time single predictions (from the API endpoint directly).
    batch_run_id        UUID,           -- FK to batch_runs added after that table is created


    -- ── Prediction Output ────────────────────────────────────
    -- Raw probability from model.predict_proba() — e.g. 0.7823
    churn_probability   NUMERIC(6,4)    NOT NULL CHECK (churn_probability BETWEEN 0 AND 1),

    -- Binary label derived from applying decision_threshold to churn_probability
    -- TRUE = model predicts this customer will churn
    churn_label         BOOLEAN         NOT NULL,

    -- Risk tier derived from churn_probability using tier thresholds
    risk_tier           risk_tier_enum  NOT NULL,

    -- The exact threshold used to produce churn_label.
    -- Stored per-prediction because the threshold may change between model versions.
    threshold_used      NUMERIC(4,3)    NOT NULL,

    -- Which model version produced this prediction.
    -- String reference (not FK) so archived model versions can be deleted
    -- from model_versions without cascading to delete all their predictions.
    model_version       VARCHAR(20)     NOT NULL,


    -- ── Explainability ───────────────────────────────────────
    -- Top 3 SHAP features for this specific prediction, stored as JSON array.
    -- Example:
    --   [
    --     {"feature": "DaySinceLastOrder", "impact": 0.22, "direction": "increases_churn"},
    --     {"feature": "Complain",          "impact": 0.18, "direction": "increases_churn"},
    --     {"feature": "Tenure",            "impact": 0.11, "direction": "reduces_churn"}
    --   ]
    -- Shown on the admin dashboard to explain WHY a customer is high risk.
    shap_top_reasons    JSONB,


    -- ── Feature Snapshot ─────────────────────────────────────
    -- The exact feature values that were fed into the model for this prediction.
    -- This is the RAW values from customer_features, before engineering/scaling.
    -- Example: {"tenure_months": 9.0, "order_count": 2, "complain": false, ...}
    -- Critical for: debugging wrong predictions, retrospective drift analysis,
    -- auditing model behaviour over time.
    features_snapshot   JSONB           NOT NULL DEFAULT '{}',


    -- ── Ground Truth ─────────────────────────────────────────
    -- The actual outcome — did this customer really churn?
    -- For Kaggle rows: seeded from kaggle_churn_label at insert time.
    -- For live rows: NULL until observed (30+ days after prediction).
    ground_truth        BOOLEAN,
    ground_truth_source VARCHAR(20)     CHECK (ground_truth_source IN (
                                            'kaggle',    -- from Kaggle dataset label
                                            'observed',  -- admin confirmed real outcome
                                            'manual'     -- manually labeled
                                        )),
    labeled_at          TIMESTAMPTZ,    -- when ground_truth was recorded


    -- ── Request Metadata ─────────────────────────────────────
    -- Whether this came from a batch scoring run or a single API call
    prediction_type     VARCHAR(10)     NOT NULL DEFAULT 'batch'
                        CHECK (prediction_type IN ('batch', 'realtime')),

    -- How long the prediction took in milliseconds.
    -- Monitored to detect model performance degradation over time.
    latency_ms          INTEGER,

    predicted_at        TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);

-- Index for "show all predictions for customer X" (customer profile page)
CREATE INDEX idx_pred_customer_id   ON predictions(customer_id);

-- Index for "show all predictions from batch run Y" (batch results page)
CREATE INDEX idx_pred_batch_run_id  ON predictions(batch_run_id);

-- Index for "show latest predictions" (dashboard, newest first)
CREATE INDEX idx_pred_predicted_at  ON predictions(predicted_at DESC);

-- Index for filtering by risk tier (dashboard "show HIGH risk customers")
CREATE INDEX idx_pred_risk_tier     ON predictions(risk_tier);

-- Index for filtering churners (churn_label = TRUE)
CREATE INDEX idx_pred_churn_label   ON predictions(churn_label);

-- Partial index: only index predictions without ground truth yet.
-- Used by the ground truth collection job: "find unlabeled predictions"
-- Partial indexes are smaller and faster than full indexes for sparse conditions.
CREATE INDEX idx_pred_unlabeled     ON predictions(predicted_at)
    WHERE ground_truth IS NULL;

COMMENT ON TABLE predictions IS
    'Complete audit trail of every prediction. '
    'features_snapshot stores exact input values for debugging and drift analysis. '
    'ground_truth is filled async: immediately for Kaggle rows, 30+ days later for live rows. '
    'batch_run_id is NULL for real-time predictions from the single-predict endpoint.';


-- ============================================================
-- SECTION 6 — TABLE 5: batch_runs
--
-- PURPOSE:
--   One row per batch scoring execution.
--   Created when the GitHub Actions cron fires POST /api/v1/predict/batch.
--   Updated as the job progresses (running → completed or failed).
--
-- WHAT READS THIS TABLE:
--   - React dashboard: "Last batch: 3 days ago, 200 customers scored, 31 HIGH risk"
--   - GitHub Actions: "did the batch complete successfully?" (alerts on failure)
--   - v_churn_trend view: line chart of HIGH risk % over time
--   - drift_reports table: references batch_run_id for each drift check
-- ============================================================

CREATE TABLE batch_runs (

    id                  UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Which model version scored this batch
    model_version       VARCHAR(20)     NOT NULL,

    -- What triggered this run
    triggered_by        VARCHAR(20)     NOT NULL
                        CHECK (triggered_by IN (
                            'scheduler',    -- GitHub Actions cron (automatic, every 20 days)
                            'manual',       -- triggered manually from GitHub Actions UI
                            'drift_alert'   -- triggered because PSI exceeded threshold
                        )),

    -- Job lifecycle timestamps
    status              batch_status_enum NOT NULL DEFAULT 'running',
    started_at          TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    completed_at        TIMESTAMPTZ,    -- set when status changes to 'completed' or 'failed'

    -- Computed column: automatically calculated from started_at and completed_at.
    -- PostgreSQL computes this on every read — you never need to set it manually.
    -- Shows how long the batch took in seconds.
    duration_seconds    INTEGER         GENERATED ALWAYS AS (
                            EXTRACT(EPOCH FROM (completed_at - started_at))::INTEGER
                        ) STORED,


    -- ── Scoring Results ──────────────────────────────────────
    -- How many customers passed the scoring eligibility gate
    -- (tenure >= 1 month AND order_count >= 1)
    customers_eligible  INTEGER,

    -- How many were actually scored (should equal customers_eligible if no errors)
    customers_scored    INTEGER,

    -- How many were skipped (new customers not yet eligible for scoring)
    customers_skipped   INTEGER,

    -- Risk tier breakdown — used for the dashboard KPI cards and bar chart
    high_risk_count     INTEGER,
    medium_risk_count   INTEGER,
    low_risk_count      INTEGER,
    onboarding_count    INTEGER,        -- customers in ONBOARDING state (not yet scoreable)


    -- ── Drift Check Results ──────────────────────────────────
    -- Evidently AI runs a PSI (Population Stability Index) check at end of each batch.
    -- PSI compares the feature distributions in this batch vs the training reference.
    -- High PSI = feature distribution has shifted = model may be unreliable.
    drift_checked       BOOLEAN         NOT NULL DEFAULT FALSE,
    drift_alert_fired   BOOLEAN         NOT NULL DEFAULT FALSE,

    -- Which feature had the highest PSI this run (the most drifted feature)
    max_psi_feature     VARCHAR(100),
    -- The PSI value of that feature
    max_psi_value       NUMERIC(6,4),


    -- ── Error Handling ───────────────────────────────────────
    -- If the batch fails, the error message is stored here.
    -- GitHub Actions reads this and creates a GitHub Issue with the error.
    error_message       TEXT,

    -- Which customer chunk the error occurred on (for debugging large batches).
    -- Batch scoring processes customers in chunks of 500 — this tells you
    -- exactly where in the process the failure happened.
    error_at_chunk      INTEGER
);

-- Index for dashboard query: "show latest batch runs" (newest first)
CREATE INDEX idx_br_started_at      ON batch_runs(started_at DESC);

-- Index for status monitoring: "find all failed runs"
CREATE INDEX idx_br_status          ON batch_runs(status);

COMMENT ON TABLE batch_runs IS
    'One row per batch scoring execution. '
    'Created when GitHub Actions POSTs to /api/v1/predict/batch. '
    'duration_seconds is a computed column — PostgreSQL calculates it automatically. '
    'drift_alert_fired triggers GitHub Issue creation in the Actions workflow.';


-- ============================================================
-- SECTION 7 — TABLE 6: drift_reports
--
-- PURPOSE:
--   Per-feature PSI values from each Evidently drift check.
--   One row per feature per batch run.
--
-- WHY NOT STORE DRIFT DATA AS JSONB INSIDE batch_runs?
--   Because you want to query drift per feature OVER TIME.
--   "Show me how DaySinceLastOrder PSI has changed over the last 6 batches"
--   → Simple SQL query on this table with WHERE feature_name = 'DaySinceLastOrder'
--   → If stored as JSONB inside batch_runs, this query is slow and complex.
--   One row per feature per run keeps the data relational and queryable.
-- ============================================================

CREATE TABLE drift_reports (

    id                  UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Which batch run this drift check belongs to
    batch_run_id        UUID            NOT NULL REFERENCES batch_runs(id),

    -- Which feature was checked (e.g. 'DaySinceLastOrder', 'Tenure')
    feature_name        VARCHAR(100)    NOT NULL,

    -- Population Stability Index value for this feature in this batch
    -- PSI interpretation:
    --   < 0.10 = no meaningful drift         → drift_level = 'none'
    --   0.10 – 0.20 = moderate drift, monitor → drift_level = 'warning'
    --   > 0.20 = significant drift, action    → drift_level = 'critical'
    psi_value           NUMERIC(6,4)    NOT NULL,

    -- Human-readable drift classification derived from psi_value
    drift_level         VARCHAR(10)     NOT NULL
                        CHECK (drift_level IN ('none', 'warning', 'critical')),


    -- ── Distribution Snapshot ────────────────────────────────
    -- Statistical summary of the feature in training data vs this batch.
    -- Stored to enable trend charts: "how has the mean shifted over time?"
    reference_mean      NUMERIC(12,4),  -- mean in the training reference dataset
    current_mean        NUMERIC(12,4),  -- mean in this batch
    reference_std       NUMERIC(12,4),  -- std deviation in training data
    current_std         NUMERIC(12,4),  -- std deviation in this batch

    checked_at          TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);

-- Index for "show all drift readings for batch run X"
CREATE INDEX idx_dr_batch_run_id    ON drift_reports(batch_run_id);

-- Composite index for the per-feature trend query:
--   "show DaySinceLastOrder drift over time"
--   → WHERE feature_name = 'X' ORDER BY checked_at DESC
CREATE INDEX idx_dr_feature_time    ON drift_reports(feature_name, checked_at DESC);

-- Partial index: only critical drift events.
-- Used by monitoring dashboard: "show me only critical alerts"
CREATE INDEX idx_dr_critical        ON drift_reports(checked_at DESC)
    WHERE drift_level = 'critical';

COMMENT ON TABLE drift_reports IS
    'Per-feature PSI drift values from each batch Evidently check. '
    'One row per feature per batch run. '
    'Enables trend charts: how is each feature drifting over successive batches? '
    'Join with batch_runs on batch_run_id to get the batch context.';


-- ============================================================
-- SECTION 8 — TABLE 7: sse_events
--
-- PURPOSE:
--   Event buffer for the SSE (Server-Sent Events) admin dashboard feed.
--
-- HOW SSE WORKS IN THIS SYSTEM:
--   1. Something happens: customer registers, batch completes, drift fires
--   2. FastAPI inserts a row into this table (delivered = FALSE)
--   3. The SSE endpoint (GET /api/v1/admin/events) polls this table every 2s
--      for rows WHERE delivered = FALSE ORDER BY created_at ASC
--   4. It sends each event to the connected admin browser client
--   5. It marks each row delivered = TRUE
--   6. A cleanup function deletes rows older than 24 hours
--
-- WHY STORE IN DATABASE INSTEAD OF IN-MEMORY ONLY?
--   Render free tier sleeps after 15 minutes of inactivity.
--   When the admin opens the dashboard after Render woke up,
--   the SSE connection reconnects and immediately receives all undelivered
--   events from the last few minutes — batch completed, new customers, alerts.
--   If events only existed in Python memory, they would be lost when Render slept.
--   The database makes the event feed reliable across connection drops.
-- ============================================================

CREATE TABLE sse_events (

    id                  UUID                    PRIMARY KEY DEFAULT gen_random_uuid(),

    -- What type of event this is
    event_type          sse_event_type_enum     NOT NULL,

    -- Flexible JSON payload — structure varies by event_type.
    -- Examples:
    --   new_customer:     {"customer_id": "uuid", "name": "...", "city_tier": 2}
    --   high_churn_alert: {"customer_id": "uuid", "score": 0.81,
    --                      "risk_tier": "HIGH", "top_reason": "DaySinceLastOrder"}
    --   batch_completed:  {"batch_run_id": "uuid", "customers_scored": 200,
    --                      "high_risk_count": 31, "drift_alert": false}
    --   drift_alert:      {"feature": "DaySinceLastOrder", "psi": 0.23,
    --                      "batch_run_id": "uuid"}
    --   model_promoted:   {"version": "v1.2.0", "auc_roc": 0.87}
    --   ping:             {}   (empty — just a keepalive signal)
    payload             JSONB                   NOT NULL DEFAULT '{}',

    -- Has the admin dashboard client received this event?
    -- FALSE = pending delivery. TRUE = sent to client.
    delivered           BOOLEAN                 NOT NULL DEFAULT FALSE,

    created_at          TIMESTAMPTZ             NOT NULL DEFAULT NOW()
);

-- Partial index: ONLY undelivered events.
-- The SSE polling query is: WHERE delivered = FALSE ORDER BY created_at ASC
-- A partial index only covers the FALSE rows — much smaller and faster
-- than a full index on delivered (which is mostly TRUE after a while).
CREATE INDEX idx_sse_undelivered    ON sse_events(created_at ASC)
    WHERE delivered = FALSE;

-- Index for the cleanup job: delete WHERE created_at < NOW() - INTERVAL '24 hours'
CREATE INDEX idx_sse_created_at     ON sse_events(created_at DESC);

COMMENT ON TABLE sse_events IS
    'Event buffer for SSE admin dashboard. '
    'FastAPI inserts rows here when events occur; SSE endpoint polls and delivers them. '
    'Survives Render sleep/wake cycles — undelivered events persist until consumed. '
    'Cleanup job deletes rows older than 24 hours.';


-- ============================================================
-- SECTION 9 — FOREIGN KEY: predictions → batch_runs
--
-- WHY IS THIS DEFINED HERE AND NOT IN THE predictions TABLE?
--   batch_runs (Table 5) did not exist yet when predictions (Table 4)
--   was created. PostgreSQL requires the referenced table to exist
--   before you can create a foreign key to it.
--   We add this FK now that both tables exist.
-- ============================================================

ALTER TABLE predictions
    ADD CONSTRAINT fk_predictions_batch_run
    FOREIGN KEY (batch_run_id)
    REFERENCES batch_runs(id)
    ON DELETE SET NULL;
-- ON DELETE SET NULL: if a batch_run row is deleted (e.g. test cleanup),
-- the prediction rows are kept but their batch_run_id becomes NULL.
-- We never want to lose prediction history just because a batch run was deleted.


-- ============================================================
-- SECTION 10 — TRIGGER: auto-update customers.updated_at
--
-- PURPOSE:
--   Every time any column in the customers table is updated,
--   automatically set updated_at = NOW().
--   Without this trigger, you would have to manually include
--   "updated_at = NOW()" in every UPDATE statement — and if you forget
--   once, the timestamp becomes stale and misleading.
--
-- HOW IT WORKS:
--   1. We define a FUNCTION that sets NEW.updated_at = NOW()
--      (NEW refers to the new row values after the update)
--   2. We attach a TRIGGER to the customers table that calls this
--      function BEFORE every UPDATE
-- ============================================================

-- Step 1: Define the trigger function
-- This function can be reused on any table that needs auto-updated_at
CREATE OR REPLACE FUNCTION trigger_set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    -- NEW is the row after the update.
    -- We set its updated_at to the current timestamp.
    NEW.updated_at = NOW();
    RETURN NEW;  -- return the modified row — this is what gets written to the table
END;
$$ LANGUAGE plpgsql;

-- Step 2: Attach the trigger to the customers table
CREATE TRIGGER customers_updated_at
    BEFORE UPDATE ON customers           -- fires BEFORE the UPDATE is applied
    FOR EACH ROW                         -- fires once per updated row (not once per statement)
    EXECUTE FUNCTION trigger_set_updated_at();

COMMENT ON FUNCTION trigger_set_updated_at() IS
    'Reusable trigger function. Sets updated_at = NOW() on every UPDATE. '
    'Attach to any table with an updated_at column.';


-- ============================================================
-- SECTION 11 — VIEW 1: v_customer_ml_features
--
-- PURPOSE:
--   The single query the ML pipeline uses to fetch all feature data.
--   Joins customers (profile) + customer_features (behavioral) into
--   one flat row per customer.
--
--   Also computes:
--     is_scoreable: whether this customer has enough history to be scored
--     days_until_scoreable: how many days until they become eligible
--
-- THIS VIEW IS THE CONTRACT BETWEEN THE DATABASE AND THE ML PIPELINE.
--   The Python batch scoring script does exactly:
--     SELECT * FROM v_customer_ml_features WHERE is_scoreable = TRUE
--   That is the entire data fetch. The view handles the join.
-- ============================================================

CREATE VIEW v_customer_ml_features AS
SELECT
    -- ── Identity (dropped before ML, kept for record-keeping) ──
    c.id                                        AS customer_id,
    c.kaggle_customer_id,

    -- ── Static Profile Features (from customers) ──────────────
    c.gender,
    c.marital_status,
    c.city_tier,
    c.preferred_payment_mode,
    c.preferred_login_device,
    c.preferred_order_cat,

    -- ── Behavioral Features (from customer_features) ──────────
    cf.tenure_months,
    cf.satisfaction_score,
    cf.complain,
    cf.warehouse_to_home,
    cf.number_of_address,
    cf.hour_spend_on_app,
    cf.number_of_device_registered,
    cf.day_since_last_order,
    cf.order_count,
    cf.order_amount_hike_from_last_year,
    cf.coupon_used,
    cf.cashback_amount,

    -- ── Scoring Eligibility Gate ───────────────────────────────
    -- A customer is only eligible to be scored if they have:
    --   1. At least 1 full month of tenure (model was not trained on new users)
    --   2. At least 1 order (otherwise behavioral features are all zero — meaningless)
    -- The Python scoring script filters WHERE is_scoreable = TRUE before calling the model.
    CASE
        WHEN cf.tenure_months >= 1
         AND cf.order_count   >= 1
        THEN TRUE
        ELSE FALSE
    END                                         AS is_scoreable,

    -- How many more days until this customer becomes scoreable.
    -- Shown on the dashboard as "Eligible for scoring in X days"
    CASE
        WHEN cf.tenure_months < 1
        THEN GREATEST(0, CEIL(30 - (cf.tenure_months * 30)))::INTEGER
        ELSE 0
    END                                         AS days_until_scoreable,

    -- ── Metadata ──────────────────────────────────────────────
    cf.features_computed_at,
    cf.features_source,
    c.registered_at

FROM customers c
-- LEFT JOIN: include customers even if they have no customer_features row yet
-- (new customers who just registered will have NULL feature values — is_scoreable = FALSE)
LEFT JOIN customer_features cf ON c.id = cf.customer_id
WHERE c.is_active = TRUE;   -- only include active customers

COMMENT ON VIEW v_customer_ml_features IS
    'Flat feature vector for ML inference. Joins customers + customer_features. '
    'Always filter WHERE is_scoreable = TRUE before passing rows to the model. '
    'This view is the contract between the database and the Python ML pipeline.';


-- ============================================================
-- SECTION 12 — VIEW 2: v_current_risk_summary
--
-- PURPOSE:
--   Pre-computed KPI summary for the admin dashboard top cards.
--   "Total customers: 214, HIGH risk: 31 (14.5%), last scored: 3 days ago"
--
--   The React frontend calls GET /api/v1/admin/overview which runs this view.
--   Re-runs on every API call — always reflects the latest predictions.
-- ============================================================

CREATE VIEW v_current_risk_summary AS
SELECT
    COUNT(*)                                                AS total_customers,
    COUNT(*) FILTER (WHERE risk_tier = 'HIGH')              AS high_risk_count,
    COUNT(*) FILTER (WHERE risk_tier = 'MEDIUM')            AS medium_risk_count,
    COUNT(*) FILTER (WHERE risk_tier = 'LOW')               AS low_risk_count,
    COUNT(*) FILTER (WHERE risk_tier = 'ONBOARDING')        AS onboarding_count,

    -- High risk percentage (of scoreable customers only — excludes ONBOARDING)
    ROUND(
        COUNT(*) FILTER (WHERE risk_tier = 'HIGH')::NUMERIC
        / NULLIF(
            COUNT(*) FILTER (WHERE risk_tier != 'ONBOARDING'), 0
          ) * 100,
        1
    )                                                       AS high_risk_pct,

    MAX(predicted_at)                                       AS last_scored_at

FROM (
    -- Use only the LATEST prediction per customer.
    -- DISTINCT ON (customer_id) keeps the most recent prediction row per customer.
    SELECT DISTINCT ON (customer_id)
        customer_id, risk_tier, predicted_at
    FROM predictions
    ORDER BY customer_id, predicted_at DESC  -- most recent first per customer
) latest_predictions;

COMMENT ON VIEW v_current_risk_summary IS
    'KPI summary for admin dashboard top cards. '
    'Uses DISTINCT ON to get only the latest prediction per customer. '
    'Returns one row with aggregate counts.';


-- ============================================================
-- SECTION 13 — VIEW 3: v_top_at_risk
--
-- PURPOSE:
--   Top 20 at-risk customers for the admin dashboard table.
--   Shows customer profile, risk score, SHAP reasons, and key features.
--   Sorted by churn_probability descending.
-- ============================================================

CREATE VIEW v_top_at_risk AS
SELECT
    c.id                        AS customer_id,
    c.full_name,
    c.gender,
    c.city_tier,
    c.preferred_order_cat,
    c.registered_at,
    p.churn_probability,
    p.risk_tier,
    p.shap_top_reasons,         -- JSONB — top 3 SHAP features (displayed as badges)
    p.predicted_at              AS last_scored_at,
    cf.tenure_months,
    cf.day_since_last_order,
    cf.satisfaction_score,
    cf.complain,
    cf.order_count
FROM (
    -- Latest prediction per customer, only HIGH and MEDIUM risk
    SELECT DISTINCT ON (customer_id)
        customer_id, churn_probability, risk_tier, shap_top_reasons, predicted_at
    FROM predictions
    WHERE risk_tier IN ('HIGH', 'MEDIUM')
    ORDER BY customer_id, predicted_at DESC
) p
JOIN customers c            ON c.id = p.customer_id
LEFT JOIN customer_features cf ON cf.customer_id = p.customer_id
ORDER BY p.churn_probability DESC   -- highest risk first
LIMIT 20;                           -- top 20 only

COMMENT ON VIEW v_top_at_risk IS
    'Top 20 at-risk customers (HIGH + MEDIUM) for dashboard table. '
    'Latest prediction per customer, sorted by churn_probability descending.';


-- ============================================================
-- SECTION 14 — VIEW 4: v_churn_trend
--
-- PURPOSE:
--   Historical churn rate per batch run for the dashboard line chart.
--   "Churn trend over the last 10 batch cycles"
--   Shows: date, customers scored, HIGH risk count and percentage.
-- ============================================================

CREATE VIEW v_churn_trend AS
SELECT
    br.started_at::DATE             AS batch_date,
    br.model_version,
    br.customers_scored,
    br.high_risk_count,
    br.medium_risk_count,
    br.low_risk_count,

    -- HIGH risk percentage of scored customers
    ROUND(
        br.high_risk_count::NUMERIC
        / NULLIF(br.customers_scored, 0) * 100,
        1
    )                               AS high_risk_pct,

    br.drift_alert_fired,           -- was there a drift alert in this batch? (shown as marker on chart)
    br.duration_seconds             -- how long did this batch take?

FROM batch_runs br
WHERE br.status = 'completed'       -- only successfully completed runs
ORDER BY br.started_at DESC
LIMIT 10;                           -- last 10 batch cycles

COMMENT ON VIEW v_churn_trend IS
    'Churn rate trend for the last 10 completed batch runs. '
    'Powers the line chart on the admin dashboard. '
    'drift_alert_fired shown as a visual marker on the chart.';


