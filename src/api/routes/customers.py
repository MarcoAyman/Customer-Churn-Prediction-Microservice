"""
src/api/routes/customers.py
══════════════════════════════════════════════════════════════════════════════
CUSTOMERS ROUTER — HTTP endpoints for customer operations.

ENDPOINTS:
  POST /api/v1/customers/register
    → Validates request, calls customer_service.register_customer()
    → Returns CustomerRegisterResponse (UUID, timestamp, initial features)
    → Used by: src/entry_form (the customer data entry form)

PHILOSOPHY — THIN ROUTES:
  This file contains NO business logic.
  It only:
    1. Receives the HTTP request
    2. Calls the appropriate service function
    3. Wraps the result in a standard APIResponse
    4. Handles service-level exceptions → correct HTTP status codes

  If you need to understand HOW registration works, read customer_service.py.
  This file only handles WHAT HTTP layer does with the result.
══════════════════════════════════════════════════════════════════════════════
"""

import logging

from fastapi import APIRouter, Depends, HTTPException, status

from database.connection import DatabaseConnection
from src.api.dependencies import get_db
from src.api.models.customer import CustomerRegisterRequest
from src.api.models.responses import APIResponse
from src.api.services.customer_service import register_customer

logger = logging.getLogger(__name__)

# ─────────────────────────────────────────────────────────────────────────────
# ROUTER
# All routes in this file are prefixed with /customers
# When mounted in main.py with prefix=/api/v1, the full path becomes:
#   /api/v1/customers/register
# ─────────────────────────────────────────────────────────────────────────────

router = APIRouter(
    prefix="/customers",
    tags=["Customers"],   # tag groups this endpoint in the /docs UI
)


# ─────────────────────────────────────────────────────────────────────────────
# POST /customers/register
# ─────────────────────────────────────────────────────────────────────────────

@router.post(
    "/register",
    response_model=APIResponse,
    status_code=status.HTTP_201_CREATED,   # 201 = Created (correct for POST)
    summary="Register a new customer",
    description="""
    Register a new customer and create their initial feature snapshot.

    **Two database rows are created in one request:**
    - `customers` row: static profile (gender, city tier, preferences)
    - `customer_features` row: initial behavioral snapshot (all zeros)

    **Returns:**
    - `customer_id`: UUID generated by PostgreSQL
    - `registered_at`: exact server timestamp of the INSERT
    - `days_until_scoreable`: 30 (tenure gate for ML scoring)
    - `initial_features`: the feature values inserted into `customer_features`

    **Side effect:**
    A `new_customer` SSE event is published to the operations dashboard live feed.
    """,
)
def register_new_customer(
    # FastAPI reads the JSON body and validates it against CustomerRegisterRequest.
    # If any field is wrong type or invalid ENUM value, FastAPI returns 422
    # automatically before this function body runs.
    request: CustomerRegisterRequest,

    # Injects a connected DatabaseConnection via the get_db dependency.
    # The connection is automatically closed when this function returns.
    db: DatabaseConnection = Depends(get_db),
) -> APIResponse:
    """
    Register a new customer.

    The route handler's only jobs:
      1. Call the service function
      2. Handle exceptions → correct HTTP status codes
      3. Wrap result in APIResponse
    """
    logger.info(f"POST /customers/register — received registration request")

    try:
        # Delegate all logic to the service layer
        result = register_customer(request=request, db=db)

        # Wrap in standard response envelope
        return APIResponse(
            success=True,
            data=result.model_dump(),   # convert Pydantic model to dict
            message="Customer registered successfully",
        )

    except ValueError as e:
        # ValueError = business rule violation (data integrity check failed)
        # Return HTTP 422 Unprocessable Entity — the data was structurally valid
        # (passed Pydantic) but failed business rules
        logger.warning(f"  Registration rejected: {e}")
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=str(e),
        )

    except Exception as e:
        # Unexpected error — log with full traceback for debugging
        logger.error(
            f"  Unexpected error during registration: {e}",
            exc_info=True,   # includes the full Python traceback in the log
        )
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail="Registration failed due to an internal error. Please try again.",
        )
