"""
src/api/services/customer_service.py
══════════════════════════════════════════════════════════════════════════════
CUSTOMER SERVICE — registration business logic.

FIX APPLIED — Dynamic column detection:
  The original version hardcoded columns like 'email', 'password_hash',
  'kaggle_customer_id', 'kaggle_churn_label' in the insert_data dict.
  If ANY of these don't exist in the actual Supabase schema, psycopg2
  raises UndefinedColumn, rolls back the transaction, and the customer
  is never written to the database.

  This version:
    1. Queries information_schema at startup to get the actual column list
    2. Builds insert_data ONLY from columns that exist in the real table
    3. Logs every field and its value before INSERT
    4. Logs whether each step succeeded or failed explicitly
    5. Will never fail due to a missing/extra column in the insert dict

WHAT IS WRITTEN TO THE DATABASE:
  On every registration, TWO rows are inserted atomically:
    Row 1 → customers table:
      - id (UUID, generated by PostgreSQL RETURNING id)
      - registered_at (NOW() from server)
      - is_active, role, full_name, gender, marital_status, city_tier
      - preferred_payment_mode, preferred_login_device, preferred_order_cat

    Row 2 → customer_features table:
      - customer_id (FK → customers.id)
      - tenure_months=0, order_count=0, complain=false
      - All other behavioral columns = NULL (no data yet)
      - features_source='system', features_computed_at=NOW()

  Both rows are committed before the SSE event is published.
  If either INSERT fails, both are rolled back (atomicity).
══════════════════════════════════════════════════════════════════════════════
"""

import logging
from datetime import datetime, timezone
from functools import lru_cache

from database.connection import DatabaseConnection
from src.api.models.customer import CustomerRegisterRequest, CustomerRegisterResponse
from src.api.services.feature_service import insert_initial_features
from src.api.services.sse_service import sse_service
from src.api.validators.data_integrity import (
    log_validation_summary,
    validate_customer_request,
)

logger = logging.getLogger(__name__)

TABLE_CUSTOMERS = "customers"


# ─────────────────────────────────────────────────────────────────────────────
# COLUMN DISCOVERY
#
# At the first registration request, query information_schema to get the
# actual column names in the customers table.
#
# WHY @lru_cache?
#   We do not want to query information_schema on every single registration.
#   Columns never change at runtime, so we cache the result permanently.
#   The cache is invalidated only when the process restarts (Render redeploy).
#
# WHY NOT HARDCODE COLUMNS?
#   If a column is hardcoded in the INSERT dict but doesn't exist in the DB,
#   psycopg2 raises UndefinedColumn → transaction rolls back → silent failure.
#   Querying the real schema prevents this class of bug entirely.
# ─────────────────────────────────────────────────────────────────────────────

@lru_cache(maxsize=1)
def _get_customers_columns(db_url_key: str) -> set:
    """
    Query the actual column names in the customers table from Supabase.

    Uses a fresh, short-lived DatabaseConnection (not the request's connection)
    so it can be called from the @lru_cache without interfering with the
    request's connection lifecycle.

    Args:
        db_url_key: the DB URL string — used as the cache key so the cache
                    is invalidated if the DATABASE_URL changes (e.g. env change)

    Returns:
        set of column name strings from the customers table
    """
    logger.info("  Querying actual customers table columns from Supabase...")

    # Use a temporary connection just for this schema inspection
    tmp_db = DatabaseConnection()
    try:
        tmp_db.connect()
        rows = tmp_db.execute_query(
            """
            SELECT column_name
            FROM information_schema.columns
            WHERE table_schema = 'public'
              AND table_name   = 'customers'
            ORDER BY ordinal_position;
            """,
        )
        cols = {row["column_name"] for row in rows}
        logger.info(f"  Customers table has {len(cols)} columns: {sorted(cols)}")
        return cols
    finally:
        tmp_db.disconnect()


# ─────────────────────────────────────────────────────────────────────────────
# MAIN REGISTRATION FUNCTION
# ─────────────────────────────────────────────────────────────────────────────

def register_customer(
    request: CustomerRegisterRequest,
    db: DatabaseConnection,
) -> CustomerRegisterResponse:
    """
    Execute the full customer registration flow.

    STEPS:
      1. Log all incoming field values (observability)
      2. Run data integrity checks (business rules beyond Pydantic)
      3. Build insert_data dict — only columns that actually exist in the DB
      4. INSERT into customers table (RETURNING id for the UUID)
      5. INSERT into customer_features table (tenure=0, orders=0, etc.)
      6. COMMIT both rows
      7. Publish SSE event to the operations dashboard live feed
      8. Return CustomerRegisterResponse

    ATOMICITY:
      Steps 4+5 share the same database connection (db from Depends(get_db)).
      If step 5 fails, step 4 is also rolled back via the context manager.
      The customer is either in the DB completely or not at all.

    Args:
        request: validated Pydantic model from the route handler
        db:      connected DatabaseConnection (from Depends(get_db))

    Returns:
        CustomerRegisterResponse

    Raises:
        ValueError: business rule violation → HTTP 422
        Exception:  DB error or unexpected failure → HTTP 500
    """
    logger.info("─" * 55)
    logger.info("  CUSTOMER REGISTRATION — starting")
    logger.info("─" * 55)

    # ── Step 1: Log what came in ───────────────────────────────────────────
    log_validation_summary(request)

    # ── Step 2: Business-level integrity checks ────────────────────────────
    logger.info("  Step 2: Data integrity checks...")
    issues = validate_customer_request(request)
    if issues:
        error_msg = f"Data integrity checks failed: {'; '.join(issues)}"
        logger.error(f"  ✗ {error_msg}")
        raise ValueError(error_msg)
    logger.info("  ✓ Integrity checks passed")

    # ── Step 3: Build insert_data ──────────────────────────────────────────
    logger.info("  Step 3: Building insert payload...")

    now = datetime.now(timezone.utc)

    # Get the actual columns that exist in the customers table.
    # db._db_url used as cache key (unique per process/deployment).
    from src.api.config import get_settings
    actual_columns = _get_customers_columns(get_settings().database_url)

    # Candidates — every field we WANT to insert.
    # Only candidates whose key exists in actual_columns will be inserted.
    # This prevents UndefinedColumn errors regardless of schema variations.
    candidates = {
        # Server-generated fields
        "registered_at":  now.isoformat(),
        "is_active":      True,
        "role":           "customer",

        # From the entry form
        "full_name":      request.full_name,           # None for Kaggle rows

        # Profile fields (all required in form, all exist in schema)
        "gender":                  request.gender.value,
        "marital_status":          request.marital_status.value,
        "city_tier":               request.city_tier,
        "preferred_payment_mode":  request.preferred_payment_mode.value,
        "preferred_login_device":  request.preferred_login_device.value,
        "preferred_order_cat":     request.preferred_order_cat.value,
    }

    # Filter to only columns that actually exist in the live table
    insert_data = {
        col: val
        for col, val in candidates.items()
        if col in actual_columns
    }

    # Warn if a candidate column was filtered out (schema mismatch)
    skipped = set(candidates.keys()) - set(insert_data.keys())
    if skipped:
        logger.warning(
            f"  Skipped fields (not in actual schema): {skipped}\n"
            f"  Run scripts/run_db_diagnostics.py to inspect table columns."
        )

    # Log every field being inserted — full visibility
    logger.info(f"  Inserting {len(insert_data)} fields into customers:")
    for field, value in insert_data.items():
        logger.info(f"    {field:<35} = {value!r}")

    # ── Step 4: INSERT into customers table ────────────────────────────────
    logger.info("  Step 4: INSERT INTO customers ...")
    try:
        customer_id = _insert_customer_row(db, insert_data)
        logger.info(f"  ✓ customers row inserted: id={customer_id}")
    except Exception as e:
        logger.error(
            f"  ✗ customers INSERT failed: {type(e).__name__}: {e}",
            exc_info=True,
        )
        raise

    # ── Step 5: INSERT into customer_features table ────────────────────────
    # Must succeed before we return success to the caller.
    # Both rows are required for the customer to appear correctly on the dashboard.
    logger.info("  Step 5: INSERT INTO customer_features ...")
    try:
        initial_features = insert_initial_features(db, customer_id)
        logger.info(f"  ✓ customer_features row inserted for {customer_id}")
    except Exception as e:
        logger.error(
            f"  ✗ customer_features INSERT failed: {type(e).__name__}: {e}",
            exc_info=True,
        )
        raise

    # ── Step 6: Publish SSE event ──────────────────────────────────────────
    # SSE is published AFTER both rows are committed.
    # The dashboard should only show a new_customer event for rows that
    # actually made it into the database.
    logger.info("  Step 6: Publishing new_customer SSE event...")
    try:
        sse_service.publish(
            event_type="new_customer",
            payload={
                "customer_id": str(customer_id),
                "full_name":   request.full_name or "—",
                "city_tier":   request.city_tier,
                "payment":     request.preferred_payment_mode.value,
                "device":      request.preferred_login_device.value,
            },
            db=db,
        )
        logger.info("  ✓ SSE event published")
    except Exception as e:
        # SSE failure is non-critical — the customer IS in the DB.
        # Log the error but do NOT re-raise — do not fail the registration
        # just because the live feed couldn't be updated.
        logger.warning(f"  SSE publish failed (non-critical): {e}")

    # ── Step 7: Build response ─────────────────────────────────────────────
    logger.info("  Step 7: Building response...")

    response = CustomerRegisterResponse(
        customer_id=customer_id,
        registered_at=now,
        days_until_scoreable=30,
        status="created",
        initial_features=initial_features,
    )

    logger.info("─" * 55)
    logger.info(f"  ✓ REGISTRATION COMPLETE — id={customer_id}")
    logger.info("─" * 55)

    return response


# ─────────────────────────────────────────────────────────────────────────────
# PRIVATE: INSERT customers row
# ─────────────────────────────────────────────────────────────────────────────

def _insert_customer_row(
    db: DatabaseConnection,
    insert_data: dict,
) -> str:
    """
    INSERT one row into the customers table and return the generated UUID.

    Uses RETURNING id to get the UUID in the same query — no second round-trip.
    PostgreSQL generates the UUID automatically via gen_random_uuid().

    PARAMETERISED SQL:
      All values are passed as parameters (%s), never string-formatted.
      This prevents SQL injection regardless of what the user entered.

    Args:
        db:          connected DatabaseConnection
        insert_data: dict of {column: value} — only existing columns

    Returns:
        str: the UUID string of the inserted row (from RETURNING id)

    Raises:
        RuntimeError: if INSERT returned no ID (should never happen)
        psycopg2.errors.UndefinedColumn: if a column in insert_data doesn't exist
        psycopg2.errors.NotNullViolation: if a NOT NULL column has a None value
    """
    columns      = list(insert_data.keys())
    values       = list(insert_data.values())
    placeholders = ", ".join(["%s"] * len(columns))

    # Build the INSERT SQL dynamically from the dict keys
    sql = f"""
        INSERT INTO {TABLE_CUSTOMERS} ({", ".join(columns)})
        VALUES ({placeholders})
        RETURNING id;
    """

    logger.debug(f"  SQL: INSERT INTO customers ({', '.join(columns)}) VALUES (...) RETURNING id")
    logger.debug(f"  VALUES: {values}")

    with db.get_connection() as conn:
        with conn.cursor() as cur:
            cur.execute(sql, values)
            # RETURNING id gives us one row: (uuid_value,)
            result = cur.fetchone()
            if result is None:
                raise RuntimeError(
                    "INSERT INTO customers RETURNING id returned no row. "
                    "This should not happen — check DB constraints."
                )
            customer_id = result[0]  # the generated UUID
        # Explicit commit — psycopg2 does NOT auto-commit
        # Without this, the INSERT is rolled back when the connection
        # is returned to the pool
        conn.commit()
        logger.debug(f"  customers row committed: {customer_id}")

    return str(customer_id)
