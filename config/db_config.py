# ─────────────────────────────────────────────────────────────────────────────
# BATCH INSERT CONFIGURATION
#
# WHY INSERT IN CHUNKS INSTEAD OF ALL AT ONCE?
#   Inserting 5,630 rows in a single INSERT statement creates one giant
#   database transaction. If it fails at row 5,000, you have to restart
#   from the beginning.
#
#   Inserting in chunks of 500 means:
#     - If a chunk fails, only that chunk needs to be retried
#     - You get progress feedback every 500 rows (important for monitoring)
#     - The database is not locked for the entire duration
#     - Memory usage is predictable and bounded
#
# CHUNK_SIZE = 500: a safe, practical size for Supabase free tier.
#   Large enough to be efficient, small enough to not hit connection limits.
# ─────────────────────────────────────────────────────────────────────────────

CHUNK_SIZE = 500  # rows per batch INSERT statement


