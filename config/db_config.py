# ─────────────────────────────────────────────────────────────────────────────
# RISK TIER THRESHOLDS
#
# Used when seeding the kaggle baseline predictions.
# For Kaggle rows we do not have a real model score — we use the binary
# churn label as a proxy score (1.0 for churned, 0.0 for retained).
# These thresholds then determine the risk tier assignment.
#
# In live predictions (Stage 3), these same thresholds are applied to
# the real model's churn_probability output.
# ─────────────────────────────────────────────────────────────────────────────

RISK_TIER_HIGH_THRESHOLD   = 0.70   # churn_probability >= 0.70 → HIGH
RISK_TIER_MEDIUM_THRESHOLD = 0.45   # churn_probability >= 0.45 → MEDIUM
                                     # churn_probability <  0.45 → LOW


def get_risk_tier(churn_probability: float) -> str:
    """
    Convert a churn probability float to a risk tier string.

    This function is defined in config (not in the seeding script) because
    the same logic is used in THREE places:
      1. seed_database.py  — when seeding kaggle baseline predictions
      2. predict.py        — when scoring live customers
      3. batch_score.py    — when running the 20-day batch cycle

    One definition → used everywhere → no inconsistency possible.

    Args:
        churn_probability: float between 0.0 and 1.0

    Returns:
        str: 'HIGH', 'MEDIUM', or 'LOW'
    """
    if churn_probability >= RISK_TIER_HIGH_THRESHOLD:
        return "HIGH"
    elif churn_probability >= RISK_TIER_MEDIUM_THRESHOLD:
        return "MEDIUM"
    else:
        return "LOW"


